def post_json(url, token, payload):  # 作用: 发送POST请求; 原理: HTTP请求+JSON序列化; 专业概念: REST API(接口调用)
    data = json.dumps(payload).encode("utf-8")  # 作用: 序列化为JSON字节; 原理: JSON编码; 专业概念: 序列化(Serialization)
    req = urllib.request.Request(url, data=data, method="POST")  # 作用: 构造请求对象; 原理: HTTP请求封装; 专业概念: 请求对象(Request Object)
    req.add_header("Authorization", f"Bearer {token}")  # 作用: 添加认证头; 原理: Bearer认证; 专业概念: 认证(Authorization)
    req.add_header("Content-Type", "application/json")  # 作用: 指定内容类型; 原理: HTTP头; 专业概念: MIME类型(MIME Type)
    try:  # 作用: 捕获HTTP异常; 原理: try/except结构; 专业概念: 异常处理(Exception Handling)
        with urllib.request.urlopen(req, timeout=30) as resp:  # 作用: 发起请求并读取响应; 原理: URL打开器; 专业概念: 超时(Timeout)
            body = resp.read().decode("utf-8")  # 作用: 读取响应内容; 原理: 字节转字符串; 专业概念: 解码(Decoding)
            return json.loads(body)  # 作用: 解析JSON响应; 原理: JSON反序列化; 专业概念: 反序列化(Deserialization)
    except urllib.error.HTTPError as exc:  # 作用: 捕获HTTP错误; 原理: 异常分支; 专业概念: HTTPError(HTTP错误)
        detail = exc.read().decode("utf-8")  # 作用: 读取错误响应; 原理: 读取错误流; 专业概念: 错误响应(Error Response)
        raise RuntimeError(detail) from exc  # 作用: 抛出运行错误; 原理: 异常包装; 专业概念: 异常包装(Exception Wrapping)


def load_existing_records(csv_path):  # 作用: 加载现有CSV记录; 原理: 读取并保留历史数据; 专业概念: 数据持久化(Data Persistence)
    if not os.path.isfile(csv_path):  # 作用: 检查文件是否存在; 原理: 文件系统检查; 专业概念: 文件存在性(File Existence)
        return {}  # 作用: 返回空字典; 原理: 无历史记录时返回空容器; 专业概念: 空字典(Empty Dict)
    records = {}  # 作用: 初始化记录字典; 原理: 用地址作为键存储记录; 专业概念: 字典映射(Dictionary Mapping)
    with open(csv_path, "r", encoding="utf-8") as f:  # 作用: 打开CSV文件读取; 原理: 上下文管理; 专业概念: 上下文管理(Context Manager)
        reader = csv.DictReader(f)  # 作用: 创建字典读取器; 原理: CSV解析为字典; 专业概念: DictReader(字典读取器)
        for row in reader:  # 作用: 逐行读取; 原理: 迭代遍历; 专业概念: 迭代(Iteration)
            address = row.get("address", "").strip()  # 作用: 获取邮箱地址; 原理: 字典取值并去空白; 专业概念: 数据清洗(Data Cleaning)
            if address:  # 作用: 过滤空地址; 原理: 条件判断; 专业概念: 非空检查(Non-Empty Check)
                records[address] = row  # 作用: 保存记录; 原理: 字典赋值; 专业概念: 键值映射(Key-Value Mapping)
    return records  # 作用: 返回记录字典; 原理: 函数返回; 专业概念: 返回值(Return Value)


def merge_records(existing, new_results, created_date):  # 作用: 合并新旧记录; 原理: 保留历史+追加新记录; 专业概念: 数据合并(Data Merging)
    merged = existing.copy()  # 作用: 复制现有记录; 原理: 浅拷贝避免修改原数据; 专业概念: 拷贝(Copy)
    for result in new_results:  # 作用: 遍历新结果; 原理: 循环处理; 专业概念: 迭代(Iteration)
        address = result["address"]  # 作用: 获取邮箱地址; 原理: 字典取值; 专业概念: 键访问(Key Access)
        if address in merged:  # 作用: 检查是否已存在; 原理: 字典成员判断; 专业概念: 成员检查(Membership Test)
            # 作用: 更新已有记录; 原理: 覆盖旧状态; 专业概念: 更新(Update)
            merged[address]["status"] = result["status"]  # 作用: 更新状态; 原理: 字典赋值; 专业概念: 状态更新(Status Update)
            merged[address]["message"] = result["message"]  # 作用: 更新消息; 原理: 字典赋值; 专业概念: 消息更新(Message Update)
        else:  # 作用: 新增记录; 原理: 插入新键值; 专业概念: 插入(Insert)
            merged[address] = {  # 作用: 构建新记录; 原理: 字典字面量; 专业概念: 记录对象(Record Object)
                "address": address,  # 作用: 邮箱地址; 原理: 字段赋值; 专业概念: 字段(Field)
                "created_date": created_date,  # 作用: 生成日期; 原理: 时间戳记录; 专业概念: 时间戳(Timestamp)
                "status": result["status"],  # 作用: 创建状态; 原理: 状态记录; 专业概念: 状态(Status)
                "message": result["message"],  # 作用: 错误信息; 原理: 消息记录; 专业概念: 消息(Message)
                "is_deleted": "否",  # 作用: 删除标记; 原理: 布尔字段文本化; 专业概念: 标记位(Flag)
                "deleted_date": "",  # 作用: 删除日期; 原理: 预留字段; 专业概念: 预留字段(Reserved Field)
            }
    return list(merged.values())  # 作用: 转换为列表; 原理: 字典值转列表; 专业概念: 值列表(Values List)


def main():  # 作用: 程序入口; 原理: 主函数组织流程; 专业概念: 主函数(Main Function)
    from datetime import datetime  # 作用: 导入日期时间模块; 原理: 获取当前时间; 专业概念: 日期时间(Datetime)
    cfg = get_config()  # 作用: 读取配置; 原理: 调用配置函数; 专业概念: 配置读取(Configuration Load)
    required = ["token", "zone_id", "dest_email"]  # 作用: 定义必填项; 原理: 列表常量; 专业概念: 必填字段(Required Fields)
    for key in required:  # 作用: 遍历必填项; 原理: 循环检查; 专业概念: 校验(Validation)
        if not cfg[key]:  # 作用: 判断缺失; 原理: 条件判断; 专业概念: 空值(Empty Value)
            raise ValueError(f"请设置 {key}")  # 作用: 抛出错误; 原理: 异常抛出; 专业概念: 参数校验(Parameter Validation)
    addresses = load_addresses(cfg)  # 作用: 生成地址列表; 原理: 调用地址生成; 专业概念: 数据准备(Data Preparation)
    enabled = cfg["rule_enabled"].lower() != "false"  # 作用: 解析启用开关; 原理: 字符串比较; 专业概念: 布尔解析(Boolean Parse)
    delay_ms = parse_int(cfg["delay_ms"], "CF_DELAY_MS")  # 作用: 解析延迟; 原理: 整数转换; 专业概念: 节流(Throttling)
    api_base = "https://api.cloudflare.com/client/v4"  # 作用: 定义API基础地址; 原理: 常量配置; 专业概念: 端点(Endpoint)
    endpoint = f"{api_base}/zones/{cfg['zone_id']}/email/routing/rules"  # 作用: 拼接接口路径; 原理: 字符串插值; 专业概念: API路径(API Path)
    created_date = datetime.now().strftime("%Y-%m-%d %H:%M:%S")  # 作用: 获取当前时间; 原理: 格式化时间戳; 专业概念: 时间格式化(Time Formatting)
    results = []  # 作用: 存放结果列表; 原理: 列表容器; 专业概念: 结果集(Result Set)
    for address in addresses:  # 作用: 遍历地址创建规则; 原理: 循环处理; 专业概念: 批处理(Batch Processing)
        payload = build_rule_body(address, cfg["dest_email"], enabled)  # 作用: 构造请求体; 原理: 函数组装; 专业概念: 请求体(Request Body)
        try:  # 作用: 捕获单条请求错误; 原理: try/except; 专业概念: 异常处理(Exception Handling)
            resp = post_json(endpoint, cfg["token"], payload)  # 作用: 调用API; 原理: HTTP POST; 专业概念: REST调用(REST Call)
            if resp.get("success") is True:  # 作用: 判断是否成功; 原理: 读取响应字段; 专业概念: 响应结构(Response Schema)
                print(f"OK: {address}")  # 作用: 输出成功; 原理: 标准输出; 专业概念: 标准输出(stdout)
                results.append({"address": address, "status": "OK", "message": ""})  # 作用: 记录成功结果; 原理: 字典追加; 专业概念: 结果记录(Result Record)
            else:  # 作用: 处理失败; 原理: 条件分支; 专业概念: 错误处理(Error Handling)
                print(f"FAIL: {address}")  # 作用: 输出失败; 原理: 标准输出; 专业概念: 标准输出(stdout)
                errors_text = json.dumps(resp.get("errors", []), ensure_ascii=False)  # 作用: 格式化错误详情; 原理: JSON序列化; 专业概念: 诊断信息(Diagnostics)
                print(errors_text)  # 作用: 输出错误详情; 原理: 标准输出; 专业概念: 标准输出(stdout)
                results.append({"address": address, "status": "FAIL", "message": errors_text})  # 作用: 记录失败结果; 原理: 字典追加; 专业概念: 结果记录(Result Record)
        except Exception as exc:  # 作用: 捕获异常; 原理: 异常兜底; 专业概念: 兜底异常(General Exception)
            print(f"ERROR: {address}")  # 作用: 输出异常提示; 原理: 标准输出; 专业概念: 标准输出(stdout)
            error_text = str(exc)  # 作用: 转成异常文本; 原理: 字符串转换; 专业概念: 异常消息(Exception Message)
            print(error_text)  # 作用: 输出异常信息; 原理: 标准输出; 专业概念: 标准输出(stdout)
            results.append({"address": address, "status": "ERROR", "message": error_text})  # 作用: 记录异常结果; 原理: 字典追加; 专业概念: 结果记录(Result Record)
        if delay_ms > 0:  # 作用: 判断是否延迟; 原理: 数值比较; 专业概念: 节流(Throttling)
            time.sleep(delay_ms / 1000.0)  # 作用: 进行延迟; 原理: 秒级休眠; 专业概念: 休眠(Sleep)
    output_csv = cfg["output_csv"]  # 作用: 获取CSV输出路径; 原理: 字典取值; 专业概念: 路径(Path)
    existing_records = load_existing_records(output_csv)  # 作用: 加载现有记录; 原理: 读取历史CSV; 专业概念: 数据加载(Data Loading)
    all_records = merge_records(existing_records, results, created_date)  # 作用: 合并新旧记录; 原理: 数据合并; 专业概念: 数据合并(Data Merging)
    with open(output_csv, "w", encoding="utf-8", newline="") as f:  # 作用: 打开CSV文件写入; 原理: 上下文管理; 专业概念: 文件IO(File I/O)
        fieldnames = ["address", "created_date", "status", "message", "is_deleted", "deleted_date"]  # 作用: 定义CSV字段; 原理: 字段列表; 专业概念: 表结构(Schema)
        writer = csv.DictWriter(f, fieldnames=fieldnames)  # 作用: 创建CSV写入器; 原理: 字段映射; 专业概念: 字段(Field)
        writer.writeheader()  # 作用: 写入表头; 原理: CSV写入; 专业概念: 表头(Header)
        writer.writerows(all_records)  # 作用: 写入所有结果; 原理: 批量写入; 专业概念: 批处理(Batch Write)
    print(f"CSV 已更新: {output_csv}")  # 作用: 输出CSV路径; 原理: 标准输出; 专业概念: 标准输出(stdout)
    print(f"本次新增: {len(results)} 条记录")  # 作用: 输出统计信息; 原理: 列表长度; 专业概念: 统计(Statistics)
    print(f"总计记录: {len(all_records)} 条")  # 作用: 输出总计数; 原理: 列表长度; 专业概念: 统计(Statistics)


if __name__ == "__main__":  # 作用: 判断是否主模块; 原理: Python入口判断; 专业概念: 主模块(Main Module)
    main()  # 作用: 调用主函数; 原理: 函数调用; 专业概念: 程序入口(Entry Point)
