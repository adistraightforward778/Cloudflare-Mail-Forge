def load_env_file(env_path):  # 作用: 加载.env文件; 原理: 逐行解析键值; 专业概念: 配置文件(Configuration File, 外部参数)
    if not env_path or not os.path.isfile(env_path):  # 作用: 校验文件路径; 原理: 条件判断+文件存在检查; 专业概念: 文件系统(File System)
        return {}  # 作用: 返回空配置; 原理: 早退出; 专业概念: 空字典(Empty Dict, 无内容集合)
    data = {}  # 作用: 初始化配置容器; 原理: 字典存储键值; 专业概念: 字典(Dictionary, 键值映射)
    with open(env_path, "r", encoding="utf-8") as f:  # 作用: 打开文件读取; 原理: 上下文管理自动关闭; 专业概念: 上下文管理(Context Manager)
        for raw in f:  # 作用: 逐行读取; 原理: 迭代器遍历; 专业概念: 迭代(Iteration, 顺序访问)
            line = raw.strip()  # 作用: 去除空白; 原理: 字符串裁剪; 专业概念: 字符串处理(String Processing)
            if not line or line.startswith("#"):  # 作用: 跳过空行/注释; 原理: 条件判断; 专业概念: 注释(Comment, 非执行内容)
                continue  # 作用: 进入下一行; 原理: 循环控制; 专业概念: continue(继续下一次循环)
            if "=" not in line:  # 作用: 过滤非法行; 原理: 字符串包含判断; 专业概念: 格式校验(Validation, 规则检查)
                continue  # 作用: 忽略非法行; 原理: 循环控制; 专业概念: continue(继续下一次循环)
            key, value = line.split("=", 1)  # 作用: 分离键和值; 原理: 字符串分割; 专业概念: 解析(Parse, 结构化读取)
            data[key.strip()] = value.strip()  # 作用: 保存键值; 原理: 字典赋值; 专业概念: 映射(Mapping, 键值对)
    return data  # 作用: 返回配置; 原理: 函数返回值; 专业概念: 返回值(Return Value)


def load_md_defaults(md_path):  # 作用: 读取md里的默认配置; 原理: 按行解析关键字段; 专业概念: 兜底配置(Fallback, 备用来源)
    if not md_path or not os.path.isfile(md_path):  # 作用: 校验md路径; 原理: 文件存在检查; 专业概念: 文件系统(File System)
        return {}  # 作用: 返回空配置; 原理: 早退出; 专业概念: 空字典(Empty Dict)
    data = {}  # 作用: 初始化容器; 原理: 字典存储; 专业概念: 字典(Dictionary)
    with open(md_path, "r", encoding="utf-8") as f:  # 作用: 打开md文件; 原理: 上下文管理; 专业概念: 上下文管理(Context Manager)
        for raw in f:  # 作用: 逐行读取; 原理: 迭代遍历; 专业概念: 迭代(Iteration)
            line = raw.strip()  # 作用: 去除空白; 原理: 字符串裁剪; 专业概念: 字符串处理(String Processing)
            if "API 令牌：" in line:  # 作用: 匹配令牌行; 原理: 子串判断; 专业概念: 模式匹配(Pattern Matching)
                token = line.split("API 令牌：", 1)[1].strip()  # 作用: 提取令牌; 原理: 字符串分割; 专业概念: 解析(Parse)
                if token:  # 作用: 校验令牌非空; 原理: 条件判断; 专业概念: 非空检查(Non-Empty Check)
                    data["CF_API_TOKEN"] = token  # 作用: 记录令牌; 原理: 字典赋值; 专业概念: 键值映射(Mapping)
            if line.startswith("CF_ZONE_ID="):  # 作用: 识别Zone ID; 原理: 前缀匹配; 专业概念: 前缀(StartsWith)
                data["CF_ZONE_ID"] = line.split("=", 1)[1].strip()  # 作用: 提取Zone ID; 原理: 字符串分割; 专业概念: 解析(Parse)
            if line.startswith("CF_DOMAIN="):  # 作用: 识别域名; 原理: 前缀匹配; 专业概念: 前缀(StartsWith)
                data["CF_DOMAIN"] = line.split("=", 1)[1].strip()  # 作用: 提取域名; 原理: 字符串分割; 专业概念: 解析(Parse)
            if line.startswith("CF_DEST_EMAIL="):  # 作用: 识别目标邮箱; 原理: 前缀匹配; 专业概念: 前缀(StartsWith)
                data["CF_DEST_EMAIL"] = line.split("=", 1)[1].strip()  # 作用: 提取目标邮箱; 原理: 字符串分割; 专业概念: 解析(Parse)
    return data  # 作用: 返回配置; 原理: 函数返回; 专业概念: 返回值(Return Value)


def get_config():  # 作用: 获取并合并配置; 原理: 环境变量 > .env > md默认; 专业概念: 配置合并(Configuration Merge)
    env_path = os.getenv("CF_ENV_FILE", ".env")  # 作用: 读取.env路径; 原理: 读取环境变量; 专业概念: 环境变量(Environment Variable)
    file_env = load_env_file(env_path)  # 作用: 读取.env内容; 原理: 调用解析函数; 专业概念: 函数调用(Function Call)
    md_env = load_md_defaults("Cloudflare批量邮箱地址.md")  # 作用: 读取md默认配置; 原理: 解析现有说明文件; 专业概念: 兜底配置(Fallback)
    def pick(key, default=""):  # 作用: 获取配置值; 原理: 本地函数封装; 专业概念: 闭包(Closure, 函数内定义函数)
        return os.getenv(key, file_env.get(key, md_env.get(key, default)))  # 作用: 按优先级取值; 原理: 多源覆盖; 专业概念: 覆盖(Override)
    return {  # 作用: 统一输出配置; 原理: 组装字典; 专业概念: 配置对象(Configuration Object)
        "token": pick("CF_API_TOKEN"),  # 作用: 读取API令牌; 原理: 配置映射; 专业概念: 认证令牌(Token, 访问凭证)
        "zone_id": pick("CF_ZONE_ID"),  # 作用: 读取Zone ID; 原理: 配置映射; 专业概念: 区域ID(Zone ID, 资源标识)
        "account_id": pick("CF_ACCOUNT_ID"),  # 作用: 读取账号ID; 原理: 配置映射; 专业概念: 账号ID(Account ID, 账户标识)
        "domain": pick("CF_DOMAIN"),  # 作用: 读取域名; 原理: 配置映射; 专业概念: 域名(Domain, 站点标识)
        "dest_email": pick("CF_DEST_EMAIL"),  # 作用: 读取目标邮箱; 原理: 配置映射; 专业概念: 目标地址(Destination, 转发终点)
        "input_file": pick("CF_INPUT_FILE"),  # 作用: 读取输入文件路径; 原理: 配置映射; 专业概念: 文件路径(File Path)
        "prefix": pick("CF_PREFIX"),  # 作用: 读取地址前缀; 原理: 配置映射; 专业概念: 前缀(Prefix, 文本前置部分)
        "start": pick("CF_START", "1"),  # 作用: 读取起始序号; 原理: 配置映射; 专业概念: 起始值(Start, 序列起点)
        "count": pick("CF_COUNT", "5"),  # 作用: 读取生成数量; 原理: 配置映射; 专业概念: 计数(Count, 数量)
        "delay_ms": pick("CF_DELAY_MS", "0"),  # 作用: 读取延迟毫秒; 原理: 配置映射; 专业概念: 节流(Throttling, 控制频率)
        "rule_enabled": pick("CF_RULE_ENABLED", "true"),  # 作用: 读取规则启用; 原理: 配置映射; 专业概念: 开关(Switch, 布尔控制)
        "output_csv": pick("CF_OUTPUT_CSV", "result.csv"),  # 作用: 读取输出CSV路径; 原理: 配置映射; 专业概念: 输出文件(Output File)
    }  # 作用: 结束配置字典; 原理: 字面量结束; 专业概念: 字典字面量(Dictionary Literal)


def random_local_part(length=8):  # 作用: 生成随机本地部分; 原理: 随机抽样; 专业概念: 随机字符串(Random String, 随机文本)
    alphabet = string.ascii_lowercase + string.digits  # 作用: 定义字符集; 原理: 字符串拼接; 专业概念: 字符集(Character Set)
    return "".join(random.choice(alphabet) for _ in range(length))  # 作用: 生成随机字符串; 原理: 迭代拼接; 专业概念: 列表推导(Comprehension)


def parse_int(value, name):  # 作用: 解析整数; 原理: 类型转换; 专业概念: 类型转换(Type Casting)
    try:  # 作用: 捕获转换错误; 原理: try/except结构; 专业概念: 异常处理(Exception Handling)
        return int(value)  # 作用: 转成整数; 原理: 内建类型转换; 专业概念: 整数(Integer, 整数类型)
    except ValueError as exc:  # 作用: 捕获格式错误; 原理: 异常分支; 专业概念: ValueError(值错误)
        raise ValueError(f"{name} 必须是整数") from exc  # 作用: 抛出友好错误; 原理: 异常链; 专业概念: 异常链(Chaining)


def load_addresses(cfg):  # 作用: 生成地址列表; 原理: 文件读取或前缀生成; 专业概念: 地址列表(Address List)
    addresses = []  # 作用: 初始化列表; 原理: 列表容器; 专业概念: 列表(List, 有序集合)
    input_file = cfg["input_file"]  # 作用: 获取输入文件; 原理: 字典取值; 专业概念: 键值访问(Key Access)
    domain = cfg["domain"]  # 作用: 获取域名; 原理: 字典取值; 专业概念: 键值访问(Key Access)
    if input_file:  # 作用: 判断是否文件模式; 原理: 条件判断; 专业概念: 条件分支(Condition)
        if not os.path.isfile(input_file):  # 作用: 校验文件存在; 原理: 文件系统检查; 专业概念: 文件系统(File System)
            raise FileNotFoundError(f"找不到输入文件: {input_file}")  # 作用: 抛出错误; 原理: 异常抛出; 专业概念: FileNotFoundError(文件不存在)
        with open(input_file, "r", encoding="utf-8") as f:  # 作用: 打开文件读取; 原理: 上下文管理; 专业概念: 上下文管理(Context Manager)
            for raw in f:  # 作用: 逐行读取; 原理: 迭代遍历; 专业概念: 迭代(Iteration)
                line = raw.strip()  # 作用: 去除空白; 原理: 字符串裁剪; 专业概念: 字符串处理(String Processing)
                if not line:  # 作用: 跳过空行; 原理: 条件判断; 专业概念: 空值检查(Empty Check)
                    continue  # 作用: 跳到下一行; 原理: 循环控制; 专业概念: continue(继续)
                if "@" in line:  # 作用: 判断是否完整邮箱; 原理: 字符串包含判断; 专业概念: 邮箱格式(Email Format)
                    address = line  # 作用: 使用完整邮箱; 原理: 直接赋值; 专业概念: 变量赋值(Assignment)
                else:  # 作用: 处理本地部分; 原理: 条件分支; 专业概念: 本地部分(Local Part, @前内容)
                    if not domain:  # 作用: 校验域名存在; 原理: 条件判断; 专业概念: 必填校验(Required Check)
                        raise ValueError("输入文件含本地部分但未设置 CF_DOMAIN")  # 作用: 抛出错误; 原理: 异常抛出; 专业概念: 参数校验(Parameter Validation)
                    address = f"{line}@{domain}"  # 作用: 拼接邮箱; 原理: 字符串插值; 专业概念: 字符串插值(String Interpolation)
                addresses.append(address)  # 作用: 追加地址; 原理: 列表追加; 专业概念: 追加(Append)
    else:  # 作用: 使用前缀或随机模式; 原理: 条件分支; 专业概念: 模式切换(Mode Switch)
        if not domain:  # 作用: 校验域名存在; 原理: 条件判断; 专业概念: 必填校验(Required Check)
            raise ValueError("使用前缀生成时必须设置 CF_DOMAIN")  # 作用: 抛出错误; 原理: 异常抛出; 专业概念: 参数校验(Parameter Validation)
        prefix = cfg["prefix"]  # 作用: 获取前缀; 原理: 字典取值; 专业概念: 键值访问(Key Access)
        start = parse_int(cfg["start"], "CF_START")  # 作用: 解析起始序号; 原理: 调用整型解析; 专业概念: 序号(Index)
        count = parse_int(cfg["count"], "CF_COUNT")  # 作用: 解析数量; 原理: 调用整型解析; 专业概念: 数量(Count)
        if count <= 0:  # 作用: 校验数量为正; 原理: 数值比较; 专业概念: 边界检查(Boundary Check)
            raise ValueError("CF_COUNT 必须为正数")  # 作用: 抛出错误; 原理: 异常抛出; 专业概念: 参数校验(Parameter Validation)
        for i in range(count):  # 作用: 生成序列; 原理: for循环计数; 专业概念: for循环(For Loop)
            num = start + i  # 作用: 计算序号; 原理: 算术运算; 专业概念: 加法(Addition)
            if prefix:  # 作用: 判断是否有前缀; 原理: 条件判断; 专业概念: 条件分支(Condition)
                local_part = f"{prefix}{num}"  # 作用: 生成本地部分; 原理: 字符串插值; 专业概念: 本地部分(Local Part)
            else:  # 作用: 无前缀时用随机本地部分; 原理: 条件分支; 专业概念: 随机生成(Random Generation)
                local_part = random_local_part()  # 作用: 生成随机本地部分; 原理: 调用随机函数; 专业概念: 随机字符串(Random String)
            address = f"{local_part}@{domain}"  # 作用: 拼接邮箱; 原理: 字符串插值; 专业概念: 字符串插值(String Interpolation)
            addresses.append(address)  # 作用: 追加地址; 原理: 列表追加; 专业概念: 追加(Append)
    if not addresses:  # 作用: 校验列表非空; 原理: 条件判断; 专业概念: 非空检查(Non-Empty Check)
        raise ValueError("未生成任何地址")  # 作用: 抛出错误; 原理: 异常抛出; 专业概念: 参数校验(Parameter Validation)
    return addresses  # 作用: 返回地址列表; 原理: 函数返回; 专业概念: 返回值(Return Value)


